#!/usr/bin/env bash
# Reads every ../schedule/*.conf entry and rewrites the REAL crontab's
# scheduler-managed block to match. This is the one and only place that
# writes cron lines for scheduler-managed jobs -- lib/sweep-loop-common.sh
# itself no longer edits crontab (see its expiry block).
#
# "Registering" a project = drop a schedule/<project>.conf (see
# ../examples/schedule-entry.conf.template) and run this script.
# "Moving hours around" = edit that file's SWEEP_CRON/BATCH_CRON and
# run this script again. "Deregistering" = delete the file and run again.
#
# Safe by default: prints what it WOULD install and exits. Nothing is
# written to the real crontab unless you pass --apply.
#
# Only ever touches lines between the BEGIN/END markers below -- anything
# else already in your crontab (including any not-yet-migrated raw entry
# like chezz's original unrestricted line) is left exactly as-is. If
# you're migrating an existing raw entry onto a schedule/*.conf file,
# remove the old raw line yourself first (crontab -e) or you'll end up
# with two entries for the same job.

set -uo pipefail

SCHED_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
SCHEDULE_DIR="$SCHED_DIR/schedule"
BACKUP_DIR="$SCHED_DIR/.crontab-backups"

MARK_BEGIN="# >>> scheduler-managed (generated by bin/sync-crontab.sh -- edit schedule/*.conf instead, then re-run) >>>"
MARK_END="# <<< scheduler-managed <<<"

APPLY=0
for arg in "$@"; do
  case "$arg" in
    --apply) APPLY=1 ;;
    -h|--help)
      echo "Usage: $0 [--apply]"
      echo "  (no args)  preview the crontab this would produce"
      echo "  --apply    back up the current crontab, then actually write it"
      exit 0
      ;;
    *) echo "unknown arg: $arg (see --help)" >&2; exit 1 ;;
  esac
done

if [ ! -d "$SCHEDULE_DIR" ]; then
  echo "no $SCHEDULE_DIR yet -- nothing to sync" >&2
  exit 0
fi

# Batch config for Tier 2 auto-staggering -- see "BATCH_CRON=auto" below.
# schedule/_batch.conf is optional (leading underscore keeps it out of the
# *.conf glob every project's entry is read from, below).
BATCH_BASE_TIME="01:00"
BATCH_STAGGER_MINUTES=30
if [ -f "$SCHEDULE_DIR/_batch.conf" ]; then
  # shellcheck disable=SC1091
  source "$SCHEDULE_DIR/_batch.conf"
fi
BATCH_BASE_MINUTES=$(( 10#${BATCH_BASE_TIME%%:*} * 60 + 10#${BATCH_BASE_TIME##*:} ))

# $1 = slot index (0-based) -> prints "MIN HOUR" for that slot, wrapping
# past midnight if the batch runs long enough to need it.
batch_slot_time() {
  local idx="$1"
  local total=$(( (BATCH_BASE_MINUTES + idx * BATCH_STAGGER_MINUTES) % 1440 ))
  printf '%d %d' "$((total % 60))" "$((total / 60))"
}

shopt -s nullglob
CONF_FILES=()
for f in "$SCHEDULE_DIR"/*.conf; do
  case "$(basename "$f")" in
    _*) continue ;;   # meta config (e.g. _batch.conf), not a project
  esac
  CONF_FILES+=("$f")
done
shopt -u nullglob

if [ "${#CONF_FILES[@]}" -eq 0 ]; then
  echo "no schedule/*.conf entries yet -- nothing to sync" >&2
  exit 0
fi

MANAGED_LINES=()
BATCH_CRON_SEEN=()   # "cron_expr|project" pairs, for collision detection
AUTO_BATCH=()         # "project|job_name|script" pairs awaiting an auto slot
ERRORS=0

is_expired() {
  # $1 = JOB_NAME. Mirrors the expiry check lib/sweep-loop-common.sh
  # itself does -- same file, same ISO-8601 string comparison.
  local job_name="$1"
  local expires_at_file="$HOME/.local/share/$job_name/expires_at"
  [ -f "$expires_at_file" ] || return 1
  local expires_at now_is
  expires_at=$(cat "$expires_at_file")
  now_is=$(date -Is)
  [[ "$now_is" > "$expires_at" ]]
}

validate_cron() {
  # $1 = cron expression. Exactly 5 whitespace-separated fields.
  local field_count
  field_count=$(awk '{print NF}' <<<"$1")
  [ "$field_count" -eq 5 ]
}

QUESTIONS_DIR="$SCHED_DIR/questions"
QUESTIONS_LINKS=()   # "project|target_path" pairs, built alongside the crontab lines below

for conf in "${CONF_FILES[@]}"; do
  # Reset every field this format understands so a conf file missing a
  # var can't silently inherit the previous file's value.
  unset PROJECT SWEEP_JOB_NAME SWEEP_SCRIPT SWEEP_CRON
  unset BATCH_JOB_NAME BATCH_SCRIPT BATCH_CRON PROJECT_REPO_PATH
  # shellcheck disable=SC1090
  source "$conf"

  name=$(basename "$conf" .conf)
  PROJECT="${PROJECT:-$name}"

  # Symlink this project's .claude/QUESTIONS.md into questions/<PROJECT>.md
  # -- "does scheduler know about this" for the one thing a human actually
  # wants to browse across every project in one place, without duplicating
  # the file (a symlink, not a copy, so either path edits the same file).
  # PROJECT_REPO_PATH is optional -- a project with no local working copy
  # in this exact shape just doesn't get one.
  if [ -n "${PROJECT_REPO_PATH:-}" ]; then
    QUESTIONS_LINKS+=("$PROJECT|$PROJECT_REPO_PATH/.claude/QUESTIONS.md")
  fi

  # --- Tier 1 (sweep): no auto-scheduling concept for this tier -- it's
  # meant to run every N minutes in a fixed window, not once nightly, so
  # "batching" doesn't apply the same way. ---
  jn="${SWEEP_JOB_NAME:-}"; sc="${SWEEP_SCRIPT:-}"; cr="${SWEEP_CRON:-}"
  if [ -z "$jn" ] && [ -z "$sc" ] && [ -z "$cr" ]; then
    : # not used for this project
  elif [ -z "$jn" ] || [ -z "$sc" ] || [ -z "$cr" ]; then
    echo "ERROR [$name/SWEEP]: SWEEP_JOB_NAME, SWEEP_SCRIPT, SWEEP_CRON must be set together (or all left blank to skip this tier) -- skipping this tier" >&2
    ERRORS=$((ERRORS + 1))
  elif [ ! -x "$sc" ]; then
    echo "ERROR [$name/SWEEP]: SWEEP_SCRIPT=$sc does not exist or isn't executable -- skipping" >&2
    ERRORS=$((ERRORS + 1))
  elif ! validate_cron "$cr"; then
    echo "ERROR [$name/SWEEP]: SWEEP_CRON='$cr' isn't a valid 5-field cron expression -- skipping" >&2
    ERRORS=$((ERRORS + 1))
  elif is_expired "$jn"; then
    echo "skipping [$name/SWEEP]: job '$jn' expired ($(cat "$HOME/.local/share/$jn/expires_at" 2>/dev/null)) -- bump EXPIRY_DAYS in its wrapper and re-run this script to renew" >&2
  else
    MANAGED_LINES+=("$cr $sc # scheduler:$PROJECT:SWEEP ($jn)")
  fi

  # --- Tier 2 (batch): BATCH_CRON="auto" (or left blank, with JOB_NAME
  # and SCRIPT both set) defers to the auto-stagger pass below, instead
  # of requiring a hand-picked, non-colliding literal time every time a
  # project registers. ---
  jn="${BATCH_JOB_NAME:-}"; sc="${BATCH_SCRIPT:-}"; cr="${BATCH_CRON:-}"
  if [ -z "$jn" ] && [ -z "$sc" ] && [ -z "$cr" ]; then
    : # not used for this project
  elif [ -z "$jn" ] || [ -z "$sc" ]; then
    echo "ERROR [$name/BATCH]: BATCH_JOB_NAME and BATCH_SCRIPT must be set together (or all three left blank to skip this tier) -- skipping this tier" >&2
    ERRORS=$((ERRORS + 1))
  elif [ ! -x "$sc" ]; then
    echo "ERROR [$name/BATCH]: BATCH_SCRIPT=$sc does not exist or isn't executable -- skipping" >&2
    ERRORS=$((ERRORS + 1))
  elif is_expired "$jn"; then
    echo "skipping [$name/BATCH]: job '$jn' expired ($(cat "$HOME/.local/share/$jn/expires_at" 2>/dev/null)) -- bump EXPIRY_DAYS in its wrapper and re-run this script to renew" >&2
  elif [ -z "$cr" ] || [ "$cr" = "auto" ]; then
    AUTO_BATCH+=("$PROJECT|$jn|$sc")
  elif ! validate_cron "$cr"; then
    echo "ERROR [$name/BATCH]: BATCH_CRON='$cr' isn't a valid 5-field cron expression (or 'auto') -- skipping" >&2
    ERRORS=$((ERRORS + 1))
  else
    BATCH_CRON_SEEN+=("$cr|$name")
    MANAGED_LINES+=("$cr $sc # scheduler:$PROJECT:BATCH ($jn)")
  fi
done

# Assign auto-batch slots in a deterministic order (by project name), each
# BATCH_STAGGER_MINUTES apart from BATCH_BASE_TIME, skipping forward past
# any slot that would exactly collide with an EXPLICITLY set BATCH_CRON
# from another project (capped so a pathological config can't loop forever).
if [ "${#AUTO_BATCH[@]}" -gt 0 ]; then
  IFS=$'\n' AUTO_BATCH_SORTED=($(printf '%s\n' "${AUTO_BATCH[@]}" | sort)); unset IFS
  slot_idx=0
  for entry in "${AUTO_BATCH_SORTED[@]}"; do
    proj="${entry%%|*}"; rest="${entry#*|}"; jn="${rest%%|*}"; sc="${rest#*|}"
    attempts=0
    while :; do
      read -r mm hh <<<"$(batch_slot_time "$slot_idx")"
      cr="$mm $hh * * *"
      collision=0
      for seen in "${BATCH_CRON_SEEN[@]}"; do
        [ "${seen%%|*}" = "$cr" ] && collision=1 && break
      done
      slot_idx=$((slot_idx + 1))
      attempts=$((attempts + 1))
      if [ "$collision" -eq 0 ] || [ "$attempts" -ge 48 ]; then
        break
      fi
    done
    BATCH_CRON_SEEN+=("$cr|$proj (auto)")
    MANAGED_LINES+=("$cr $sc # scheduler:$proj:BATCH ($jn, auto-batched)")
  done
fi

# Warn (not fail) on two projects' Tier 2 sharing an identical cron
# expression -- both still run; the note is a nudge to stagger by hand
# (or switch to BATCH_CRON=auto). Auto-assigned slots are already
# collision-checked above, so this mainly catches two EXPLICIT times
# clashing.
if [ "${#BATCH_CRON_SEEN[@]}" -gt 1 ]; then
  for ((i = 0; i < ${#BATCH_CRON_SEEN[@]}; i++)); do
    for ((j = i + 1; j < ${#BATCH_CRON_SEEN[@]}; j++)); do
      cr_i="${BATCH_CRON_SEEN[$i]%%|*}"; name_i="${BATCH_CRON_SEEN[$i]#*|}"
      cr_j="${BATCH_CRON_SEEN[$j]%%|*}"; name_j="${BATCH_CRON_SEEN[$j]#*|}"
      if [ "$cr_i" = "$cr_j" ]; then
        echo "WARNING: $name_i and $name_j both run Tier 2 at '$cr_i' -- consider staggering (edit BATCH_CRON in either schedule/*.conf, or set BATCH_CRON=auto)" >&2
      fi
    done
  done
fi

if [ "$ERRORS" -gt 0 ]; then
  echo "-- $ERRORS error(s) above; the affected tier(s) were left OUT of the generated crontab, everything else proceeded --" >&2
fi

# Sync questions/<PROJECT>.md symlinks -- same preview-by-default,
# --apply-to-write rule as the crontab itself below. Each symlink points
# at a project's own .claude/QUESTIONS.md so it's browsable from one
# place (this directory) without duplicating the file.
QUESTIONS_HEADER='# Questions for the user

Running log, appended to (never overwritten or trimmed) by `/bug-sweep`
and `/nightly-batch` whenever something bigger than a routine tracker note
comes up. Clear an entry by deleting its line once you'"'"'ve actually read
and dealt with it; that'"'"'s the only thing that should ever remove
something from this file.
'

if [ "${#QUESTIONS_LINKS[@]}" -gt 0 ]; then
  if [ "$APPLY" -eq 0 ]; then
    echo "--- questions/ symlinks that would be created/updated (pass --apply to actually do this) ---"
    for entry in "${QUESTIONS_LINKS[@]}"; do
      proj="${entry%%|*}"; target="${entry#*|}"
      echo "questions/$proj.md -> $target$([ -f "$target" ] || echo "  (target does not exist yet -- would be created with a header)")"
    done
  else
    mkdir -p "$QUESTIONS_DIR"
    for entry in "${QUESTIONS_LINKS[@]}"; do
      proj="${entry%%|*}"; target="${entry#*|}"
      if [ ! -f "$target" ]; then
        mkdir -p "$(dirname "$target")"
        printf '%s' "$QUESTIONS_HEADER" > "$target"
        echo "created $target"
      fi
      ln -sfn "$target" "$QUESTIONS_DIR/$proj.md"
      echo "linked questions/$proj.md -> $target"
    done
  fi
fi

CURRENT_CRONTAB="$(crontab -l 2>/dev/null || true)"

KEPT="$(awk -v b="$MARK_BEGIN" -v e="$MARK_END" '
  $0==b {skip=1; next}
  $0==e {skip=0; next}
  skip {next}
  {print}
' <<<"$CURRENT_CRONTAB")"

{
  printf '%s\n' "$KEPT"
  echo "$MARK_BEGIN"
  for line in "${MANAGED_LINES[@]}"; do
    echo "$line"
  done
  echo "$MARK_END"
} > /tmp/sync-crontab.$$.new

if [ "$APPLY" -eq 0 ]; then
  echo "--- preview only (pass --apply to actually install) ---"
  cat /tmp/sync-crontab.$$.new
  rm -f /tmp/sync-crontab.$$.new
  exit 0
fi

mkdir -p "$BACKUP_DIR"
BACKUP_FILE="$BACKUP_DIR/$(date -Is).bak"
printf '%s\n' "$CURRENT_CRONTAB" > "$BACKUP_FILE"
echo "backed up current crontab to $BACKUP_FILE"

crontab /tmp/sync-crontab.$$.new
rm -f /tmp/sync-crontab.$$.new
echo "crontab updated. current state:"
crontab -l
